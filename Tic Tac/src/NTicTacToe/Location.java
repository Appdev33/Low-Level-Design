package NTicTacToe;

public class Location {
	int xVal;
	int yVal;
	
	public Location(int xVal, int yVal) {
		this.xVal = xVal;
		this.yVal = yVal;
	}

	public int getxVal() {
		return xVal;
	}

	public void setxVal(int xVal) {
		this.xVal = xVal;
	}

	public int getyVal() {
		return yVal;
	}

	public void setyVal(int yVal) {
		this.yVal = yVal;
	}
	
	
}
